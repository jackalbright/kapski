<p>This Code of Ethics is an expression of proper conduct in the pursuit of our industry as related to the public, our fellow contractors, our employees and this organization. </p>
<h3>Obligation of the Painting Contractor </h3>


<h3>Obligation of the Contractor </h3>
<ul class="codeOfEthics">

<li>To provide painting and decorating services in a professional business-like manner with both craftsmanship 
and up to date techniques. </li>
<li>To protect, preserve and decorate homes, edifices, facilities and all other public and private property with 
the most progressive and efficient means and materials. </li>
<li>To maintain regulatory, insurance and licensing requirements in the areas we conduct business. 
</li> 
</ul>
 
<h3>Obligation to the Public </h3> 
<ul class="codeOfEthics">
<li>To conduct our business operations according to the highest professional and industry standards.  </li>
<li>To provide innovative solutions which enhance value. 
</li>
<li>To faithfully fulfill all contracts. </li>
<li>To expand our knowledge through the constant study of best business practices and utilization of the latest 
technical advances in our profession and industry. </li>
<li>To conduct one's self in a professional manner at all times. </li>
<li>To follow federal, state, and local tax laws and regulations.</li> 
<li>To adhere to federal, state, and local laws and administrative regulations regarding the use  and disposal 
of paints, coatings, and related materials.</li> 
</ul>

<h3>Obligation to Our Fellow Members</h3>  
<ul class="codeOfEthics">
<li>To encourage the free exchange of ideas.</li> 
<li>To meet with fellow members and promote all the precepts of this Code. </li>
<li>To present a broad and favorable image of our profession and industry at all times.</li> 
<li>To promote equal treatment of all members. </li>
<li>To oppose any discriminatory practices.</li> 
<li>To not disseminate misinformation or derogatory information concerning our association or related entity, 
member or industry organization.  </li>
 
</ul>
<h3>Obligation to Our Employees </h3> 

<ul class="codeOfEthics">
<li>To provide opportunities for training, employment, career advancement and economic security in a 
progressive manner to all those who would apply themselves. </li>
<li>To provide a safe working environment by establishing good safety practices in compliance with federal, 
state, and local laws and administrative regulations. </li>
<li>To follow federal, state, and local employment laws and regulations.</li> 

</ul> 
<h3>Obligation to Our Organization</h3>  

<ul class="codeOfEthics">
<li>To promote the spirit of this Code and the objectives of our profession and industry through creation of and 
participation in Councils, Chapters, Forums, all association entities and industry related groups. </li>
<li>To establish and maintain the highest level of reciprocal respect and goodwill with all segments of the 
industry. </li>
<li>To promote our association to customers, prospective members, the industry, and related groups. 
</li>
</ul>


