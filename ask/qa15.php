<p><span class="QA">Q</span>We have dark streaks on our roof that have appeared over the last couple of years. Shingles are cedar color. Our home is a rancher with a hip roof and 4-inch pitch. Streaks are unsightly, and we don't know what procedure to follow to get rid of them. You may have addressed this issue previously. If so, I missed it. Don't know why because I read you faithfully. The "stain" is, of course, black algae. The problem began surfacing several years ago when asphalt-shingle manufacturers switched from rags to ground limestone as a filler material, roofers say.
</p>
<p><span class="QA">A</span>Black algae really loves the stuff, with all those nutrients, and shady spots on the roof supply constant moisture, keeping the limestone damp and helping the algae grow.
</p>

<p>Mildew, that green stuff, feeds off the black algae.</p>
<p>Black algae typically takes about 10 years to begin appearing, about the time the 10-year manufacturer's warranty on algae expires. Some shingle manufacturers have been adding waste from copper processing to the limestone filler because copper has been proven to deter algae growth.
</p>
<p>Roofers say copper tends to be the best deterrent to the start and growth of black algae, although zinc, too, is sometimes used.
</p>
<p>Roofers nail copper or zinc strips at the peak of the roof above the portion affected by the black algae. When it rains, water reacts with the copper strip, and what is produced (this isn't a chemistry class) continues down the roof to kill the algae, and the stain disappears.</p>

<p>While some roofers think copper is better, zinc is what most suppliers sell. Other metals also appear to work to some degree, though not consistently, at least from observations.
</p>
<p>The obvious question, of course: Is the black algae or the green mildew damaging your shingles? The mildew simply makes your roof slippery to walk on.
</p>
<p>Black algae? Roofers and shingle suppliers seem to say no.
</p>
<p>The official policy of CertainTeed of Valley Forge, I've been told, is that the black algae is an aesthetic problem only, and roofers agree with that assessment.
</p>

