<p><span class="QA">Q</span>How Should I Prepare My Home for Painting?</p>
<p ><span class="QA">A</span>I recommend your follow these guidelines:</p>
<p>If you plan on painting the inside of your home, here's tips on how to prepare your home for the painters and what to expect from Kapski Painting.
</p> 
<p>For best results, please complete any renovations prior to the painting work. If you have scheduled us to do this work before the painting, we will assume the responsibility for the proper sequencing of the project.
</p> 
<p>If you are going to refinish the wood floors, this should be done AFTER the painting.
</p>
<p>The curing process of the floor coating usually takes a few weeks. The coating remains soft for some time and this limits our use of stepladders etc. A quality floor contractor will be careful of the walls and the baseboard.
</p> 
<p>Please remove all of the smaller items from the workspaces. If we are doing multiple rooms, we can offer suggestions on how best to accomplish this. The TV, DVD or entertainment center, and all associated wiring, should be removed from the work space.
</p>
<p>Smaller items such as photos, pictures hung on walls, carpeting, padding, lamps, lights, smaller furniture, nick-knacks, and window treatments should be removed too.
</p> 
<p>If you would prefer us to empty the rooms, please let us know 24 hours in advance. There will be a small charge to remove and reset items. Before the job starts, we will cover all items left in the rooms with new plastic film and clean canvas drop cloths. Upon completion, we will vacuum all of the rooms.
</p> 
<p>If you are not going to rehang art in the same places, please remove fasteners. This will tell us to patch the holes so that you can start fresh. If you are going to hang the art in the same places, then no action is needed, except to remove the art or photos.
</p> 
<p>The preparation necessary for a high quality job will include patching and smoothing out all of the surfaces. This can create dust. Sealing off the work areas will be done by the Kapski Painting team, but be prepared for small amounts of dust afterwards which you will want to clean up after we have left.
</p> 
<p>Please contact us well in advance of the project regarding the colors specified for each room; ceiling, walls, trim, closets, etc. Provide us with the manufacturer’s name, color name, number, and sheen. Any remaining paint will be left on site in sealed, marked cans.
</p> 
<p>We recommend NO or Low VOC paints for all interior work because they do not emit unhealthful odors or off-gassing. The day after using these paints, you’ll be able sleep in the room with no negative effects.
</p> 
<p>In hot or cold weather, please condition the workspaces so that the humidity is low and the temperatures are within a reasonable range to allow for patch and paint drying within a reasonable amount of time.
</p> 
<p>Please keep the recently painted doors away from their frames to allow for drying. When painted, wood swells and will dry quickly if out of contact with other surfaces. On the day we paint your bathroom, please do not use it that morning. Please refrain from bathing or showering until 36 hours after the room has been painted.
</p> 
<p>Now, sit back, relax and enjoy your freshly painted beautiful home!
</p>

