<p><span class="QA">Q</span> Are there Eco-Friendly paint colors that aren't  boring?</p>
<p ><span class="QA">A</span> Plenty of them. Eco-Friendly coatings don't have to be boring. </p>
<h3></h3>
<p>There aren't many choices for green coatings and many manufacturers have the same old boring colors! They don't seem have made the leap regarding the new technologies. So what are your options?</p>

<p>Check out the Sierra Greenhome site at <a href="http://www.sierragreenhome.com">www.sierragreenhome.com</a>. Kapski Painting is a charter member of this great organization and we want to help spread the word.</p>

<p>We have learned a lot about some of the new colors and certifying systems so that you can be sure that the paint you are putting in your home is safe, long lasting and beautiful.</p>

<p>Greenguard Environmental Institute is one of the best third party certifiers around (<a href="http://www.greenguard.org">www.greenguard.org</a>)</p>

<p>Greenseal was founded in 1989. <a href="http://www.greenseal.org">www.greenseal.org</a>. It is a science based environmental organization that uses the new GS-11 standard which certifies that the paint tested is free from a long list of bad stuff. Check these certifiers out if you care about off-gassing, indoor air quality and related issues.</p>

<p>But the fun part, for me at least, is the color selections. Matt, one of our lead painters loves the AFM Safecoat Paint color chart. I agree with him...these colors are the best! They are high quality and VOC free...but the colors are great!</p>

