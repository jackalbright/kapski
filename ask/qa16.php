<p><span class="QA">Q</span>Do you have any suggestions for cleaning walls and ceilings?
</p>


<p><span class="QA">A</span>Of course! Here are a few:
</p>

<h3>Rub a Dub Dub</h3>
<p>Walls require more routine cleaning than ceilings, mainly because it's a lot easier for fingerprints and scuff marks to land on them. Using a brush attachment, vacuum walls when you clean the room. Go behind pictures and mirrors with the small brush attachment. Remove cobwebs monthly or as needed.</p>
<p>When you vacuum, be careful not to press cobwebs against the wall. When you're ready to wash the wall, use an all-purpose cleaner for cleaning washable walls. Test the product to make sure it does not harm your wall covering by washing an inconspicuous place first. Kapski Painting likes to use Washable Flat Enamels which are easy to spot clean and to clean the entire room as well.
</p> 
<p>Here are some additional suggestions:</p>
?<ul>
<li>Wash walls from the bottom to the top, overlapping the cleaned areas to prevent streaks.
</li>
<li>To prevent water from running down your arm when washing walls, make a bracelet from a sponge or washcloth held in place with a thick rubber band.
</li>
<li>Lift crayon marks off a painted wall by rubbing them carefully with a cloth or sponge dampened with mineral spirits or lighter fluid. Remove any shine by sponging lightly with hot water.
</li>
<li>?To remove transparent tape from a wall without marring the paint or wallpaper, use a warm iron. Through a protective cloth, press the tape to soften and loosen its adhesive backing.
</li>
<li>Remove smudges while they are fresh, but do not scrub with much pressure or use synthetic scouring pads or abrasive cleansers.
</li>
<li>?To clean textured walls, old nylon stockings are better than sponges or cloths because they won't tear and leave difficult-to-remove bits and pieces on the surface.
</li>
<li>Slight smoke stains above a fireplace opening are quickly removed with abrasive cleanser. Scrub the powder into the moistened brick and then rinse well with clear water to make sure that no white residue remains. If cleaning changes the color of the brick, even out the color by rubbing another brick of the same color over the discolored surface.
</li>
</ul>

 
<p>Remember...you don't need nor should you use harsh chemicals for any reason inside or outside your home. If you are having a problem cleaning or can't find a more gentle method please ASK STEVE!

