<h3>Sealing your driveway the safe way</h3>

<p>For a long time, many Americans have sealed their
driveways with coal-tar-based sealant, both for appearance and
weatherproofing. Here's the rub: It's bad for you, and it's bad for the
environment.</p>
<p>Coal-tar-based sealants, mostly found east of the Mississippi, usually
contain high amounts of chemicals called polycyclic aromatic hydrocarbons,
or PAHs, which the U.S. Geological Survey says "are probable human
carcinogens and ... are toxic to fish and other aquatic life." Those
sealants flake off over time. In a recent study, the geological survey found
that the dust in apartments next to parking lots that had been coated with
coal-tar sealant had concentrations of PAHs that were 25 times higher than
in apartments next to parking lots coated with other sealants. And if you've
coated your driveway with the same sealant, you're tracking the same dust
into your own house.</p>

<p>Some governments are taking the findings seriously: Washington, D.C., has
banned that kind of sealant. So have Austin, Texas; Dane County, Wis.; and
White Bear Lake, Minn.</p>

<p>What to do instead: If you really want to seal your driveway, look for
asphalt emulsion-based sealant, which has "far, far fewer" PAHs in it, says
Nancy McClintock, assistant director of Austin's watershed protection
department. Other products free of PAHs exist, but they're rare, McClintock
says, and much more expensive.</p>

<p>Or go another route: Don't put anything on at all. The manufacturers say
that applying sealant protects the driveway and makes it last longer, but
"we've never seen hard data that necessarily proves that," McClintock says.</p>
