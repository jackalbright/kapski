<h3> Protect  Your Home from Water Damage - A Checklist</h3>

<p>Outside Your Home</p>

<ol>
<li>Keep the roof free from leaves, twigs, and other litter to allow for proper drainage.</li>
<li>Make sure air can flow freely through all soffit and roof vents. This will reduce the buildup of heat and moisture and help extend the life of the roof. Kapski can install plastic and aluminum vents.</li>

<li>Consult a professional on using a preservative or cleaner (depending on the type of roof you have) to help limit the weathering effects of moisture and retard growth of molds and mosses.</li>

<li>Keep trees trimmed to prevent them from rubbing against the roof or providing excessive shade.</li>
<li>Replace missing, curling, cupping, broken, or cracked shingles.</li>
<li>Watch for damage in valley areas of the roof, and around the flashing at chimneys, vents, and other junctions.</li>
<li>Check your attic around flues, plumbing vents, and chimneys for roof leaks, especially if you've noticed water stains on
m.. the ceiling.</li>
</ol>

<ol>

<li>Clean debris from your gutters and downspouts, and inspect them regularly. Think about installing gutter shields if your gutters frequently fill with debris.</li>
<li>Place splash blocks at the end of downspouts to carry water away from the foundation, or add an extra length of downspout if necessary.</li>
 <li>Every spring, have the air conditioning (A/C) system serviced by a qualified contractor. Make sure their service includes inspecting and cleaning the A/C condensation pan drain line. Change the air filters on a regular basis.</li>
<li>Before winter starts, disconnect garden hoses from all spigots and turn off each spigot's water supply.</li>
<li>Replace any damaged caulk around windows or doors.</li>
<li>Repaint wood siding as needed.</li>
<li>Fill in any low spots next to the house to help water drain away from the foundation.</li>

</ol>

<h3>Hardware that can help</h3>
<p>Even after taking the preventions we've discussed here, check frequently around the house for signs of leaks. It's important to find them before they cause serious damage. Of course, it's impossible to keep watch for signs of trouble 24 hours a day. Fortunately there are water leak detection systems that can help.</p>

<h3>Water alarms</h3>

<p>These systems are usually battery-operated, stand-alone units. The moisture sensor on the device will sound an alarm when it senses moisture.
Water alarms are inexpensive and easy to install. They can be placed on the floor or mounted on a wall. They should be located in high-risk areas such as under sinks and near appliances and equipment that use water.
This device can help only if someone is inside the home, hears the alarm, and takes action.</p>

<h3>Individual appliance systems</h3>

<p>These systems are installed on a specific appliance and will automatically shut off the water supply in case of a leak.
Depending on the type of device, you may be able to install this system without any special tools. However, in some cases, a qualified plumber may be needed.</p>
<h3>Whole-house systems</h3>

<p>These systems feature a shut-off valve that is installed on the main water supply pipe. When a leak is detected, the system will automatically shut off the entire water supply.
If you travel often, this type of system could help you rest assured while you are away from home.
Whole-house systems typically take between four and six hours to install and a qualified plumber is normally required.</p>

    

