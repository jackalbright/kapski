<h3 >Is the Next Big Thing Really Smaller Houses?</h3>


<p>The median size of new single-family houses sold in the U.S. has increased 42% during the past 20 years - from 1,650 square feet in 1978 to 2,335 square feet in 2007.</p>

<p>But a closer look at the data shows that this trend has not held steady throughout from year to year. Decreases occurred in 1981-1982, 1995, 2003 and 2007-2008, and I expect house sizes to decrease even further this year and next while the economy remains sluggish. In addition, with the $8,000 first-time home buyer federal tax credit stimulus bringing more first-time home buyers to the market, the trend toward smaller houses will probably continue.
</p>
<p>So, does this mean that every builder should begin building 1,600-square-foot houses?
</p>
<p>Certainly not. Buyers will continue to buy at different price levels and want different sized homes.
</p>
<p>However, I do see this trend toward smaller homes playing out on a sliding scale - with most builders introducing products that are between 10% and 20% smaller than their current offerings.
</p>
<p>This isn't the only path builders are taking. Many are also scrutinizing other aspects of their business in an effort to cut costs and boost market appeal.
</p>
<p>Some suggestions they may want to consider include the following:
</p>
<h4>Site Planning - Mix Product Types on Smaller Lots
</h4>
<p>If it is still feasible, builders and developers can adjust their site plans so that they can accommodate the approved and entitled number of units on smaller lots and set aside more land as dedicated open space. Builders should consider mixing product types on the site, as well, by adding such products as twins and townhouses into the mix.
</p>
<p>These changes are consistent and compatible with other emerging trends that appeal to new home buyers - such as more walkable and diverse neighborhoods and green building practices. In fact, the smaller, smarter home can be considered a precursor to green building since, by definition, green homes require less material to build and less energy to heat and cool.
</p>
<h4>Space Programming - Less Formal, More Flexible</h4>
<p>As for the house itself, the trend among builders and buyers continues to be moving toward re-purposing rooms and interior spaces. To a certain extent, this began to emerge even before the economy went south.
</p>
<p>For instance, builders have been phasing out many of the formal rooms in houses smaller than 2,000 square feet and providing buyers with flexible options. In many of these houses, the dining room, if it is available at all, is often offered as a "dining or den" space, with doors as an option.
</p>
<p>For houses 2,200 square feet and larger, what in the past may have been the living room, parlor or other formal room now is often offered as a "den/guest bedroom" with a full bathroom nearby.
</p>
<p>As these formal rooms shrink in size, builders are devoting additional space to great rooms and kitchen/breakfast areas - the rooms that home owners and families are using more.
</p>
<p>Builders and architects also are offering space for what used to be an upstairs bedroom as a loft or other form of flexible space, which can be converted into an additional bedroom, if that's what the buyer wants.
</p>
<p>Builders also are scaling back on many of their amenities, but not all of them. Turned staircases, open kitchens and spacious master suites with separate showers and tubs are still in demand.
</p>
<p>Likewise, builders are proving new spaces with functions that spice up and bring new conveniences to living in smaller houses. These space-type amenities include "drop zone" desks near the kitchen, closets near the garage entrance and smaller "desk niches" tucked throughout the house.
</p>
<p>Finally, contrary to popular belief, some volume ceilings continue to be popular, although in more limited settings. Keep in mind that these are the amenities that distinguish new housing products from re-sales.
</p>
<h4>Elevation and Structure - Don't Oversimplify
</h4>
<p>Some builders and architects are returning to the "value box" concept of elevations and plans - a straightforward plan shape with minimal jogs and corners - to cut costs. But this is not something I would recommend unless it is a "disguised value box" and includes enough detail to project an elegant elevation.
</p>
<p>Such a design approach typically lets one large gable establish the home's presence, with quality materials used as accents and to create the most impact. Taking a cue from the traditional neighborhood design concepts, builders are returning to having usable front porches with details that enable them to dress up the house at a minimal cost.
</p>
<h4>Smaller Houses - Short or Long-Term Trend?
</h4>
<p>Will house sizes continue to decrease once the housing market stabilizes? Demographics make a strong argument for the affirmative, since the average household size is continuing to shrink.
</p>
<p>Some in the industry have even predicted that the trend toward smaller house sizes will continue through the coming decade, but I'll pass on that prediction.
</p>
<p>For the near-term, however, expect house sizes to continue to shrink, at least until the economy and housing market recover.
</p>
