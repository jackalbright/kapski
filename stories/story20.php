<h3>How to get ready for Kapski Painters (and what to expect-
</h3> 
<p>This explanation of the Interior painting process includes what to expect from us and how to prepare your home for the painters. Please read!
</p> 
<p>For best results please complete any renovations prior to the painting work. If you have scheduled us to do this work in advance of the painting we will assume the responsibility for the proper sequencing of the project.
</p> 
<p>If you are going to refinish the wood floors this should be done after the painting. The curing process of the floor coating usually takes a few weeks’ time. The coating remains soft for some time and this limits use of stepladders etc. A quality floor contractor will be careful the walls and baseboard.
</p> 
<p>Please remove all of the smaller items from the workspaces. If we are doing multiple rooms we can offer suggestions on how best to accomplish this.
</p> 
<p>The TV, DVD or Entertainment center and all associated wiring should be removed from the work space.
</p> 
<p>Smaller items include photos, pictures hung on walls, carpeting, padding, lamps, lights, smaller furniture, nick-knacks and window treatments.
</p> 
<p>If you are not going to rehang art in the same places, please remove fasteners, this will tell us to patch the holes so that you can start fresh. If you are going to hang the art in the same places then no action is needed except to remove the art or photos.
</p> 
<p>If you would prefer us to empty the room please let us know 24 hours in advance. There will be a small charge to remove and reset items.
</p> 
<p>We will cover all items left in the room with new plastic film and clean canvas drop cloths. We will vacuum the room upon completion.
</p> 
<p>The preparation necessary for a high quality job will include patching and smoothing out surfaces. This can create some dust. You should be prepared to do the final cleanup of small amounts of dust after the painters have left. Sealing off the work areas will be done by the Kapski Painting team but be prepared for small amounts of dust afterwards.
</p> 
<p>The homeowner should have contacted Kapski Painting well in advance of the project regarding the colors specified for each room; ceiling, walls, trim, closets, etc. We should have the manufacturer's name, color name and number and sheen. We will leave any remaining paint on site in sealed, marked cans.
</p> 
<p>We love to specify NO or Low VOC paints for all interior work. You will be happy the way these products do not emit unhealthful odors or off-gassing. You could sleep in a room done the previous day with no effects.
</p> 
<p>In hot or cold weather please condition the workspaces so that the humidity is low and the temperatures are within a reasonable range to allow for patch and paint drying within a reasonable amount of time.
</p> 
<p>Please keep the recently painted windows and doors away from their frames to allow for drying. Wood swells when painted and will dry quicker if allowed to remain out of contact.
</p> 
<p>Your Bathroom should not be used in the morning if we are painting it that day. You should not take a bath or shower until 36 hours after the room has been painted.
</p></div>