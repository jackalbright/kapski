<p>This Code of Ethics is an expression of proper conduct in the pursuit of our industry as related to the public, our fellow contractors, our employees and this organization. </p>
<h3>Obligation of the Painting Contractor </h3>
<ul class="codeOfEthics">
<li>To provide the services of the industry to the fullest extent of our technological 
ability. </li>
<li>To protect, preserve and decorate edifices, facilities and all other properties with 
means and materials of the most progressive and efficient type. </li>
</ul>
<h3>Obligation to the Public </h3>
<ul class="codeOfEthics">
<li>To conduct our individual operations according to the highest professional standards. </li>
<li>To faithfully fulfill all contracts. </li>
<li>To develop through continual study the highest degree of technical knowledge of 
our industry. </li>
</ul>



<h3>Obligation to Our Fellow Members </h3>
<ul class="codeOfEthics">
<li>Encourage the free exchange of ideas. </li>
<li>To meet with fellow members and promote all the precepts of this code. </li>
<li>To present a broad and favorable image of our industry across our nation. </li>
<li>To promote equal treatment of all painting contractor members. </li>
<li>To oppose any discriminatory practices against any of our units or members. </li>
</ul>



<h3>Obligation to Our Employees </h3>
<ul class="codeOfEthics">
<li>To provide opportunities for training, employment, career advancement and economic security in a protracted and progressive manner to all those who would apply themselves. </li>
</ul>

<h3>Obligation to Our Organization </h3>
<ul class="codeOfEthics">
<li>To promote the spirit of this code and the objectives of our industry through creation of councils and chapters. </li>
<li>To establish and maintain the highest level of reciprocal respect and goodwill with all segments of the industry. </li>
</ul>
