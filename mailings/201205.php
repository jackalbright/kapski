<h3>Consumer Reports Reveals Best Paint Recommendations</h3>

<p><strong>PITTSBURGH (KDKA)</strong> - Now that it's spring, you're probably planning some home improvement projects, and a can of paint is one of the cheapest ways to give your house a brand new look.
</p>
<p>However, before you head to the store, you'll want to see what Consumer Reports found in its latest tests of almost 60 interior paints as it might save you time and money.
</p>
<p>Contractor Bill Bradsell said paying more for high-quality paint can actually be a money saver. "It's more economical because a lot of times we can do less coats," Bradsell said. Consumer Reports tested to see how well paints cover in one coat. Testers evaluated dozens of interior paints that cost anywhere from $8 a gallon to more than $60.
</p>
<p>They paint over increasingly darker stripes and found big differences.
</p>
<p>Another important test is how well paints resist stains. Testers applied a greasy mixture and let it sit overnight.
</p>
<p>Then, a machine scrubbed the paint to see how well the grease came off. Some came clean in just a few passes. But, the cheapest paint in the tests, Color Place from Wal-Mart, didn't clean nearly as well as some of the best paints.
</p>
<p>Another question was what type of interior paint to get?
</p>
<p>"Satin paint, which is also called eggshell, is the best way to go for most walls," Rico de Paz said.
</p>
<p>Among satin paints, Benjamin Moore Aura Satin earned the highest score, but it costs more than $60 a gallon.
</p>
<p>For about half the price, Behr's Premium Plus Ultra Satin Enamel from the Home Depot did just about as well.
</p>
<p>Both did a great job covering in one coat, as well as resisting stains.
</p>
<p>If you prefer a flat paint, Behr Premium Plus Ultra Flat at $33 a gallon scored the highest, with Behr's Premium Plus version rated a best buy at just $20 a gallon. Among semi-gloss paint, there was a tie, between Benjamin Moore's Regal Select at $50 a gallon, and Behr Premium Plus Ultra Semi-Gloss at $34 a gallon.
</p>
<p>There's another plus with the recommended paints as well. They were all rated excellent to very good at self-priming, so they can go over bare wood and wallboard without a coat of primer, which will save you time and money.
</p>

