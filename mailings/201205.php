<h3>Your Spring To-Do List</h3>
<p>Why Exterior Painting Should Be First on your spring to-do list</p>
<p><strong>PHILADELPHIA, Mar 15, 2012 (BUSINESS WIRE)</strong> - As the springtime weather shifts from messy to mild, every homeowner's attention turns to the out of doors. It's time to clean things up, tend to the garden, and make needed repairs to both the home and its surroundings. Where to start? Assuming that your exterior paint is failing, it's best to focus on that first, according to experts.
</p>
<p>Debbie Zimmer, paint and color expert for the Paint Quality Institute, says there are plenty of good reasons to start spring chores with exterior painting:
</p>
<p>"First, spring is a very comfortable time to do outdoor painting. Second, it's smart to paint before putting down mulch, which along with your plants, will just get trampled if you paint later on. Third, why not get your painting done before more pleasant 'distractions' like gardening, sports, and barbecues begin?"
</p>
<p>Zimmer says that if your house paint is near the end of its life expectancy, you're taking a chance by postponing repainting. It doesn't take long for exposed wood to begin to rot, and other types of exteriors also suffer when the paint wears off. Wait too long and you may have to make repairs before starting to paint.
</p>
<p>Another reason to get to your painting first: Exterior latex paint forms the most durable, protective finish when the weather is mild. "It's always best to do exterior painting when the temperature is above 50 degrees F., but not too hot," says Zimmer. "Very hot days can cause the paint to dry too quickly and impair good paint film formation. By painting in moderate weather, you'll likely get a longer-lasting paint job."
</p>
<p>If a day starts off mild, but turns very hot, try to avoid painting in direct sunshine, since sunlit surfaces can be 10 to 20 degrees hotter than the air temperature. Work your way around the house so that you are always painting in the shade. As a bonus, you'll be more comfortable!
</p>
<p>When painting, pick a day that isn't too windy. Like the heat of the sun, wind can cause latex paint to dry too quickly and prevent optimal paint film formation. Plus, wind can stir up dust and other contaminants that can imbed in the paint to create an inviting surface for mildew, which feeds on such matter.
</p>
<p>You should also try to steer clear of "weather events" that could affect the paint, waiting for another day if it has rained within the last 24 hours, or postponing the job if several days of rain are expected right after you finish painting.
</p>
<p>Of course, it's important to properly prepare the surface before doing any exterior painting. That includes applying a coat of primer to any new surface that has never been painted, or spot-priming previously painted surfaces where the paint is worn away.
</p>
<p>To extend the life of your paint job, Zimmer recommends that you apply the very highest quality 100% acrylic latex paint, which is especially durable, flexible and colorfast. Top quality paint often lasts 10 years or more, compared to about four years for ordinary paint, saving you time, work, and money in the long run. For the longest-lasting paint job, always apply at least two coats - either a coat of primer and a coat of paint, or two coats of house paint.
</p>
<p>Once you've finished your exterior painting, you can turn your attention to the other things on your to-do list. What's more, you'll have peace of mind knowing that you've done right by your biggest investment - your home.
</p>
