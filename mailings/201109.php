<ul>
<li>Using one stain color to create a mottled look.</li>
<li>Creating a faux-finished look with color enhancers.</li>
<li>Creating a glossy, consistent color throughout the floor.</li>
<li>Using multiple colors to create a layered look.</li>
<li>Getting a variegated look by using both stains and dyes.</li>
<li>Creating the natural look of leather.</li>

</ul>
<p>Each technique is covered in detail to help consumers choose the appropriate look for their upcoming project. From interior home and retail floors, to exterior patios, driveways and walkways, stained concrete continues to grow in popularity, with new innovative techniques becoming available regularly. Ask a Kapski Painting Representative for more information.</p>

