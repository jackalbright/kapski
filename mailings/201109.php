<h3>Are you color-phobic?</h3>


<p>Sonu Mathew, senior interior designer at Benjamin Moore &amp; Co., offers a few tips for the paint-challenged.</p>

<p>Start small: Paint a powder room or guest room where you can play with color without trepidation.</p>

<p>Sample it: Lighting and the existing colors in your floors, furniture and architectural elements influence how color looks. Paint a color you like on foam core or poster board so you can move the sample from dark to light corners and between rooms.</p>

<p>Keep it simple: People want to use multiple colors in a palette, but don't know how to address open floor plans and color change. Consistent trim, molding and continuous ceilings create a visual flow. Stick with three to five colors, depending on your square footage and space allocation, and use elements such as a fireplace wall or an architectural niche to separate them. Consider a color change to break up large spaces by function (for example, living room to kitchen).</p>

<p>Quality: The paint you use is as important as the color. Kapski Painting recommends a low-VOC (volatile organic compounds) paint that's self-priming and requires no more than two coats.</p>

