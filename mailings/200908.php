<h1  class="heySteve">Hey Steve!</h1>
<p>One more question this month, this one from Jack A in Kennett Square, PA, who asks:</p>
<p><span class="QA">Q</span> How can I improve my hole drilling technique, seems like I have a knack for splintering the wood and getting less than straight holes?</p>

<div class="signature">
<p>Thanks,<br />
Jack Albright - Kennett Square, PA</p>
</div>

<p><span class="QA">A</span>When I need to bore an especially clean hole, such as one for a screw plug, I typically apply the simple principle of opening up my wallet - I use pricey, precision-machined brad-point drills that I normally can count on for crisp work.
But with certain woods, such as mahogany, even the sharpest cutters leave an unsightly frayed edge. And while up-shear geometry is great for evacuating shavings, it can drag debris past the surface fiber, causing splintering.</p>

<h3>Here's what I do to avoid the problem:</h3>
<ol>
<li>Start the hole with the driver/drill running in reverse to produce down-shear cutting. This pushes the waste wood away from the face of the work piece while compressing surface fibers at the circumference of the hole.
</li>
<li>Finish boring with the driver/drill rotating normally.
</li>
<li>Unless you use a drill press, the hole will likely be 1/64-inch or so out of round.
</li>
<li>In all but the fussiest of circumstances, this amount will be negligible.
</li></ol>
<p>If this method is new to you, try the technique on a few pieces of scrap to get a feel for it.
</p></div>

