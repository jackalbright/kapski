<h1 class="askSteve">Ask Steve</h1>
<p><span class="QA">Q</span> Last month's Q&A about wallpapering costs was great! What can you tell us about interior paint pricing?</p>
<p ><span class="QA">A</span> Here are some guidelines for interior paint pricing:</p>
     <h3>Interior Paint Pricing</h3>
          <p>A can of fresh paint can take a room from looking dreary and dated to a sense of sparkling newness for a relatively low price compared to other remodeling projects.</p>

<h4> Typical costs:</h4>
<ol>
<li>Interior paint itself costs anywhere from $12-$50 or more a gallon, depending on the quality. Painting a 12x12-foot room, both walls and ceiling, can require $25-$100 worth of paint, plus $10-$50 for primer, brushes, rollers, drop clothes and other supplies. For a 1,500-square-foot home, supplies run $200-$300 for average paint, and $400-$600 for higher quality.</li>
<li>Expect to pay $200-$400 to have a 12x12-foot room painted by a licensed contractor with brand-name paints; a 15x20-foot room or larger runs $300-$700 or more; and a 1,200-1,500 square foot home is $1,100-$2,000 or more. Having the ceiling painted bumps the cost to the high end of the scale.</li>
</ol>

