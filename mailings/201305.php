<h3 class="storyBoxHeading">Selling your house soon? Call Kapski!</h3>

 
<p>Here are some simple homeowner tips that will generate a nice return on investment:</p>
<ul>

<li>Make sure that your house number is easily visible from the street.
</li>
<li>Set out some large house plants on the porch and/or walkway in attractive containers. These add interest and character right where potential buyers will be entering and exiting the home.
</li>
<li>Weed and spread fresh mulch in all the flower beds and around the bushes.
</li>
<li>Neatly trim all bushes, pruning those that especially look out of control.
</li>
<li>Plant brightly colored annuals.
</li>
<li>Power-wash the siding, gutters and fascia. A clean house makes all the difference!
</li>
<li>Put a coat of fresh paint on the front entry and garage door.
</li>

</ul>
<p>If anything looks weathered like mailboxes, exterior lighting fixtures or house numbers consider replacing or painting
them. And, of course, call Kapski! </p>

