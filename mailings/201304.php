<h3>The White Roof</h3>
In Greece, they paint the roofs white to reflect the heat and cool the building. New York City has taken similar measures, painting white the black tops of buildings and providing a significant energy savings. 

<p>&quot;If all flat black roofs in the world today were replaced by white ones the impact would be the same as taking half the cars in the world off the road for the next 20 years or shutting down 500 medium sized coal-fired power plants, or 1,000 gas-fired ones.&quot; - <a href="http://new.usgbc.org/articles/good-doctor">US Green Building Council</a></p>

<p>If you have a building with a hot, black roof, we'll paint it white for you! Save energy. Go green with white!
