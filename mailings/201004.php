<h1>Ask Steve</h1>


<p><span class="QA">Q</span>Does the new Lead Law that takes effect April 22 2010 include the outside of my house?  We have two children that play outside and we want to be sure that the flaking paint on our old house won't be a problem for them when we have the work done either by us or a contractor. <br />Thanks, John from Media</p>
<p><span class="QA">A</span>

When it comes to working around lead paint it's always a good idea to call a pro, one that is certified by the EPA. This way you can be sure that he or she knows exactly what to do regarding to removal of the lead paint from the siding and trim and how to control the process. There are tools that can and cannot be used; some can kick up lead dust. By law, any contractor working on the interior or the exterior of a house suspected of having lead paint must be certified by the EPA. Removal techniques and cleanup specifications are spelled out and I recommend that anyone wanting a healthy home environment follow these guidelines. While the EPA estimates these requirements will raise renovation costs by no more than $100.00 per job some contractors say it'll be more like $800.00 to $1200.00. If you want more information visit <a href="http://epa.gov/">epa.gov</a>.</p>
