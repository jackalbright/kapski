<h3 class="storyBoxHeading">3 Reasons to give spray foam insulation a try</h3>

<p>From www.reliableremodeler.com - Posted by Conrad ~ August 14, 2012
</p>

 
<p>As one of those people who are resistant to change and think most new technology will probably end up as a passing fad, I'm always a bit dubious when a new building material happens along. Needless to say, I was less than enthusiastic a few years back when a private home inspector, for whom I was doing a remodeling project, wanted to use spray foam insulation in his home's exterior walls rather than good old-fashioned fiberglass batts.
</p>
<p>I had used the spray foam insulation available in small aerosol cans to fill in gaps around windows and doors in the past, but had never seen the product used to insulate an entire home. However, never let it be said that I'm not willing to broaden my horizons — by the end of the project I was a convert to the benefits spray foam insulation could provide homeowners and contractors.
</p>
<h4>Say goodbye to air infiltration</h4>

<p>One of the most frequent warranty issues I've had to deal with as a remodeling contractor is air infiltration or drafts and a large percentage of the time it's due to loose, missing, or torn fiberglass insulation. The insulation in the exterior walls can look perfect during a close-in inspection, but all it takes is one careless sheetrock hanger to invite outside air into the home. Spray foam insulation can pretty much eliminate that problem as it may look like the wall has been slimed, but it's there to stay.
</p>
<h4>Lower heating and cooling costs</h4>

<p>These days every penny counts so when the government's Energy Star site states that homeowners may be able to save up to 20 percent on their heating and cooling costs by upgrading their insulation and ensuring the home is properly sealed, it may be worth a listen. The advantage of spray foam insulation is that it fills every nook and cranny in your exterior walls — even those that the insulation contractor feels are too tight to chink with fiberglass. Spray foam can be used to seal around plumbing vents, utility penetrations, and even around electrical outlets to eliminate pesky drafts.
</p>
<h4>Do big things in small places with spray foam insulation</h4>

<p>Fiberglass batt insulation is a great product and has been used in many homes, but the R-value it can provide is dictated by the thickness of your exterior walls. If your home has 3 1/2 inch outside framing, standard batt insulation is normally R-13. However, using closed cell spray foam in the same space may be able to increase your insulation protection to R-22 or even higher depending on the product's composition.
</p>
<p>Don't be a Luddite like I was — if you're planning a remodeling project or just an energy saving upgrade, give serious consideration to spray foam insulation.
</p>



