<h3 class="storyBoxHeading">Exterior Color Advice</h3>

<h4>1. Honor History</h4>
<p>If youre planning to paint an older home, you'll probably want to use a historically accurate color scheme. You can hire a pro to analyze old paint chips and recreate the original color. Or, you can refer to historic color charts and select shades that might have been used at the time your home was built.
</p>
<h4>2. Jazz Up the Past</h4>
<p>In some neighborhoods, homeowners fly in the face of history. Instead of choosing historically accurate colors, they paint their houses modern colors to dramatize architectural details. Using bright colors on old architectural details can produce startling and exciting results. But before you buy 10 gallons of bubblegum pink, its a good idea to look at what your neighbors are doing. A fluorescent colored Victorian that looks splendid in San Francisco will seem wildly out of place in more conservative neighborhoods.
</p>
<h4>3. Consider Your Neighbors</h4>
<p>The house next door can give you paint color ideas, but dont copy your neighbor exactly. Choose colors that set your house apart, without clashing with nearby buildings.
</p>
<h4>4. Borrow From Nature</h4>
<p>The landscape around your house is blooming with color ideas. Trees may suggest an earthy palette of greens and browns. A beach setting might suggest vivid blues, turquoises, and coral colors. Even the garden in your front yard can inspire exciting color combinations.
</p>
<h4>5. Check the Roof</h4>
<p>Your house is your canvas, but it is not blank. Some colors are already established. What color is your roof? Your paint color doesnt need to match the roof, but it should harmonize.
</p>
<h4>6. Look For Things That Wont Be Painted</h4>
<p>Every home has some features that will not be painted. Does your house have brick walls? Vinyl windows? A natural wooden door? Will steps and railings remain their existing colors? Choose a color scheme that harmonizes with colors already present on your house.
</p>


</p>
