<h3 class="storyBoxHeading">Beware Artillery Fungus</h3>

 
<p>We got a tech bulletin from our trade group the PDCA which warns homeowners not to use certain mulches near their homes or outbuildings. Even a parked car can get hit. We successfully completed a residential exterior... a couple of weeks later we got a call from the homeowner complaining of little black spots on the front of her newly painted clapboard and wood trim.
</p> 
<p>The source was a fungus that, when temperatures and humidity levels reach a certain level this fungus shoots out thousands of spores which cling tenaciously to any surface near its source. it is extremely difficult to remove. I was able to produce the information with photos taken of a different house showing the same damage. The homeowner absolved us of the damage.
</p> 
<p>So, be careful of the mulch you use. I recommend Licorice Root. It doesn't contain the fungus. At least not yet!
</p>


</div>

<div class="storyBox">
<h3 class="storyBoxHeading">From The Paint Quality Institute-</h3>
 
<p>Surfactant Leaching happens on the exterior and interior of your house when certain environmental conditions occur. In your bathroom or Kitchen or on your siding as well when humidity is high and chemicals rise to the face of the coat of paint looking like blotches or streaks.
</p> 
<p>It is not your fault, the paint's fault, or the contractors fault. Please <a href="http://www.paintquality.com/homeowners/paint-advice/infosheets/surfactantleaching.pdf">read this article</a> if you have this problem. </p></div>
