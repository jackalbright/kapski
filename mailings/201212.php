<h3 class="storyBoxHeading">Seven secrets for building sustainable homes on a budget.</h3>
<p>From: ECOHOME 2012 Posted on: November 20, 2012<br />
By Jennifer Goodman</p>

 
<p>In the affordable housing sector, energy efficiency isnt an add-on luxuryits a necessity that helps to keep the cost of homeownership within reach of low-income families, who spend 17 percent of their income on utility bills, says Matt Clark of Habitat for Humanity International. In comparison, middle-income families pay about 4 percent.
"Green building makes complete and utter sense for us," he says.
</p>
<p>Building pros in the affordable and for-profit sector gathered last week at the 2012 Greenbuild International Conference and Expo to pick up sustainable construction knowledge from the nonprofit, which builds 66,500 houses each year in the U.S. and abroad. Mike Haigh of the Dallas Area Habitat for Humanity (HFH) provided these tips:
</p>
<ul>
<li>Build small to keep costs down. Dallas Area HFH houses average 1,262 square feet, almost 700 square feet less than the national average.
</li>
<li>Dont rely on technology to combat a problem that can be fixed with good design, such as blasting the air conditioning in a room with too much southern exposure.
</li>

<li>Place the HVAC system and hot water heater in a centrally located space to keep duct runs and piping short and eliminate air leakage. Dallas Area HFH places the hot water heater within 15 feet of all fixtures and specs duct runs of no more than 10 to 15 feet. That way the air handler doesnt have to work so hard to push air over a shorter distance," Haigh said.
</li>

<li>Provide natural light whenever possible, especially in small, enclosed spaces. Find a way to squeeze a window into every room.
</li>

<li>Spec 36-inch-wide doorways and 42-inch-wide hallways for future aging-in-place needs. That way its not a costly expense to figure out ways to make the house accessible," he said.
</li>
<li>Rely on advanced framing techniques such as 24 inches o.c. framing, two-stud corners, ladder blocking, and engineered trusses to save money on lumber and maximize structural integrity. Haigh noted that this takes a little extra time on the front end, but you save money on the construction and the buyer saves money on operating costs."
</li>
<li>Tightly seal the entire building envelope, including an encapsulated attic, careful flashing, and premium energy-efficient windows.
</li>
</ul>
<p>By considering a few simple, inexpensive techniques builders can add to the energy and resource efficiency of their projects, Haigh concluded. "Call it what you want: common sense, feng shui, or building science," he said. In the long run it leads to a much better product."
</p>

