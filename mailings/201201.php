<h3>BATHROOM STAINING</h3>
<p>We recently repainted a Bathroom (with poor ventilation) and brown stains showed up on the paint even though we used the best paint made specifically for this environment. Here is a word about this issue from the Paint Quality Institute:</p>


<p>Question:<br />
What is the dark stuff I get on the ceiling of my bathroom? It is in small areas about the size of a small coin, and is brownish in color. It is on parts of the ceiling, particularly over the shower area.</p>

<p>Answer:<br />
This is material extracted from a latex paint by the high humidity and moisture common in a bathroom, particularly after the shower has been used. Gradually, some of the thickener and surfactants commonly used in making latex paint are extracted and left in concentrated form on the surface of the paint. This generally does not hurt the durability of the paint, and rather is an issue of appearance. It can usually be removed by careful cleaning with a sponge and detergent with warm water. If you encounter this when you go to repaint the bathroom, be sure to remove as much of it as you can, before applying primer or paint. Be sure to rinse off all residual detergent before proceeding. Kitchen &amp; Bath paints are in general formulated to minimize surfactant leaching. Allow the paint to dry at least overnight before using the shower; preferably allow several days' drying time.</p>



