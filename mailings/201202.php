<h3>Colorful Hallways and Stairways</h3>
 HOLLY O'DELL    Posted on Jan. 04, 2012

<p>In their overall design scheme, homeowners often treat hallways as an afterthought. They're seen as a means to a destination, not a destination in and of themselves. However, hallways can be the perfect canvas to take a chance on color. "Since the hallway is usually a transitional space, and not someplace where you'll be spending a lot of time, you can go just a little wild and crazy," says Kati Curtis of Nirmada Interior Design.
</p>
<p>One big draw of designing hallways is that the wall color generally doesn't have to compete with other design elements such as furniture or windows, says designer Lori Dennis of Los Angeles. "It's a smaller space so you can focus your efforts, versus doing a huge project like painting a special graphic or a whole wall in a 20'x20'  room," she says. "With a small amount of cost, labor, material and time, you can make a huge impact."
</p>
<p>Even though hallways represent a place to have some design fun, they can also offer their share of design challenges. One is proportion. "In many cases, ceiling height is much taller than the width of the hallway, creating a tunnel effect," notes Curtis. "To counteract it, I'll use dark colors on the hallway ceiling and lighter colors on the walls to make the space feel a little more proportional and intimate."
</p>
<p>No matter how creative you get in hallways, be sure your color palette provides the proper transition to surrounding rooms. Ask Steve Kapski to consult here, he has the experience and the eye for color.
</p>
<p>Developing a focal point also can address issues of proportion. Dennis pays attention to hallway ceilings. "Whether you're painting a ceiling a flat color or creating a pinstripe or graphic, it brings the eye up and turns a boring, uneventful space into an experience."
</p>
<p>Lighting plays another important role. "Oftentimes hallways are not lit very well, so even a great paint color won't show up properly without adequate lighting," says Acker, who's used recessed, track and picture lighting as additional illumination sources. "It's a safety matter as well, because you have to be able to see as you're progressing down a hallway."
</p>
<p>When it comes to entry halls, Dennis takes cues from iconic architect Frank Lloyd Wright. "He thought that hallways were paramount to the arrival experience," she says. "Wright always made entrances rather low and dark so that when you entered the next room it would appear higher and lighter."
</p>
<p>No matter how creative you get in hallways, be sure your color palette provides the proper transition to surrounding rooms. "If the living room is a deep turquoise blue, for example, painting the hallway a lighter hue of that same color acts as a preview to the main space," Curtis says. "It's subtle and very effective."
</p>
<p>Adds Dennis: "Hallways are the appetizers of the meal, the precursor to the following rooms, so you do need to make sure they jibe with everything else going on."
</p>

