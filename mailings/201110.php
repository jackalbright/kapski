<h3>Interiors: Pick the perfect paint to perk up your walls:</h3>

<p>Are you getting ready to paint to freshen up your home for fall or the holidays -- or just because it's time to do something with those walls?
</p>
<p>Here are some thoughts to help you select the right finish.
</p>
<p>Color options are many, as you surely have noticed, but once you've selected the right color, you have another choice. Do you purchase it in flat, semi gloss or gloss? MAB Paints make a low sheen washable flat enamel which we at Kapski Painting have been recommending for years. We have had excellent results with this line.
</p>
<p>Ah, that is a good question. Your choice will determine how vivid details appear, and how easy it will be to clean that wall when necessary.
</p>
<p>Glossier or shinier finishes have higher levels of resin and lower levels of pigmentation, while the less-shiny finishes have more pigment than resin. (That's just a by-the-way remark to keep in the back of your head or to sound intelligent around your friends.)
</p>
<p>First know that the terminology differs from one brand of paint to the next. "Flat" might be called "matte." This paint is a nonreflective and can be washable. Then there is an eggshell finish, or "low luster." This one is more washable than flat or matte and has a very slight sheen. Next in line: satin finish. This one has a definite sheen to it, and can be hard to distinguish from semi gloss. Actually, some brands call the same finish either "sheen" or "semi gloss." How's that for confusion? Then there is full-gloss paint. This one is the shiniest of all and also the most durable.
</p>
<p>Generally, the higher the gloss is, the more washable the paint. Flat paint isn't going to be as washable as gloss, but all can be washed. The difference is how much you think the walls in that room will need to be washed.
</p>
<p>There are also differences in how each finish looks. Being nonreflective, a flat or matte finish will disguise surface imperfections and rough wall surfaces the best. As you go up in shine, more imperfections will show. In other words, a very glossy finish will highlight any and all imperfections a wall may have.
</p>
<p>Usually, selecting a satin finish instead of a gloss is a good compromise, because the satin is washable enough but won't highlight any flaws on the wall. Woodwork that is in good shape does well and looks classy when semi gloss is applied. A front door will be dramatic with a semi gloss finish, as well.
</p>
<p>When using a semi gloss or gloss finish, add a little Floetrol or a similar product to the paint before applying it to the wall. The Floetrol will help the paint flow on more smoothly and will also minimize brush or roller marks
</p>
