<h3 class="storyBoxHeading">A 21st Century look at homeownership.</h3>

<p>Owning a home used to be the standard investment for retirement. You bought when you were in your ’20s, then sold in your ’60s. Forty years of steadily increasing value meant a nice chunk of money to reinvest and live off of when you stopped working. That was the American dream! Now it’s more like the American nightmare.
</p>
<h3>Homeownership and the current economy
</h3>
<p>In this economy, people cannot be guaranteed that a new home will increase in value. In the US, there are still 16 million homes ‘under water’, which means they have lost so much value that the mortgage is more than what the house is worth on the market. Homes may decrease in value even more as foreclosures threaten to further empty those neighborhoods. With this scenario, is buying a home a good investment anymore? Do potential or current homeowners have any reason to be confident?
</p>
