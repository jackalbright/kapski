</p> 
<p>Do you think enough of your painter to refer him to friends, family and business associates? Most of our clients do, and we show our appreciation by making it worth their while to do so. Would you like to have your front door repainted a different color, perhaps an hours worth of touch ups throughout your house? We really do appreciate your reference and we show it through our services offered.
</p> 
