<h3 class="storyBoxHeading"> Do-It-Yourself Repair Tips</h3>
    
<p>Here are a few helpful tips to keep in mind when you're doing your own repairs to appliances, fixtures, and systems in your home:</p>

<p>Use a digital camera, or make a sketch of how things fit together before you take them apart so that you will know how to reassemble them. As you begin to disassemble a unit, line up all the parts in the exact order in which they were removed. That will keep everything in order and make reassembly easy.</p>

<p>Take the worn or defective parts with you when you go to the hardware store or home improvement center so you can find an exact replacement.</p>

<p>Rubber washers, gaskets, small fittings, and other inexpensive parts that have a limited life should be replaced along with the defective part. Otherwise, you may find yourself disassembling everything for another repair in the near future. When you are replacing something that has several inexpensive parts-such as a sink trap-it oftentimes pays to replace all of the components.</p>

