<h1 class="askSteve">Did You Know?</h1>
<p> The heating unit went out on my dryer. Our repairman  told us that he wanted to show us something, and he went over to the dryer and pulled out the lint filter. It was clean. (We always clean the lint from the filter after every load.) He took the filter over to the sink and ran hot water over it. The lint filter is made of a mesh material. I'm sure you know what your dryer's lint filter looks like. Well, the hot water just sat on top of the mesh! It didn't go through it at all!</p> 
<p>He told us that dryer sheets cause a film to form over the mesh, and that's what burns out the heating unit. You can't <em>see</em> the film, but it's there. It's the substance that's in the dryer sheets, the purpose of which is to make your clothes soft and static free.</p>
<p>
 The best way to keep your dryer working for a very long time (and to keep your electric bill lower) is to take that filter out and wash it with hot soapy water and an old toothbrush (or other brush) at least every six months. That makes the life of the dryer at least twice as long! How about that? Learn something new everyday! I certainly didn't know dryer sheets would do that, so, I thought I'd share! </p>
<p>Note: I  tested my screen by running water on it. The water ran through a little bit but mostly collected  in the mesh screen. After washing it with warm soapy water and a nylon brush, the water ran right through the screen! That repairman knew what he was talking about! </p>


