    <h3 class="storyBoxHeading">'Yardening' by Liz Ball</h3>
    
    <p>
    Column From the Springfield Press March 26, 2014<br>
    Used with permission</p>
     
    <h4>A Painter's perspective</h4>
     
    <p>The plants, soil, insects, birds and other wildlife in home landscapes represent a complex natural system in your yard. However things are even more complex where the natural system meets the manmade system on the property which is near the house.
    </p> 
    <p>Probably no one is more familiar with this area, or zone than the people you hire to paint the outside of your house. So, I will be consulting from time to time with Steve Kapski, who owns a painting company and is a plant lover, about the insights he has gained during his years of experience in the zone.
    </p> 
    <p>Professional painters typically collect the paint scrapings from the preparation process, but not all do.  There is a chance that the lead from the old paint and the scrapings from it could leach into the soil from rainfall and the snow melt. Then it is likely that the soil around the foundation of the house has lead in it.
    </p> 
    <p>To find out, use a soil test kit available at box stores and paint stores to determine if there is a problem. If so, do not plant anything in the affected area.
    </p>

