<h1>January 2014</h1>
<h3 class="storyBoxHeading">Cold Enough?</h3>
<p>You have all heard my rant about the amount of <a href="http://www.nrdc.org/thisgreenlife/0905.asp" target="_blank">VOCs</a> in paint. Aside from the noxious odor it can make you sick. This winter season we are not using much conventional paint but going with No-VOC Paints. What a difference! For a few dollars per gallon more you would be amazed at the difference! Very little odor, it's wonderful for us to work in such an environment.</p>
<p>If you struggle with the thought of the whole house smelling like paint this just isn't the case. There are a few good sources in the marketplace. We know who they are. You can find out for yourself online. But either way, make the switch; do it yourself or have it done professionally. It's so worth it. Here is a great alternative to conventional paints...Ceramic Paint. <a href="https://www.olearypaint.com/shop/product_ceramic.html">Check it out here.</a></p>
 


