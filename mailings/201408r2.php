<h4>Bamboo plants</h4>

<p>Almost everyone, even a non- plant person, recognizes bamboo growing in yards, parks and public gardens. Its many forms and colors are an exotic and attractive feature in these areas, especially Asian themed gardens.
</p>
<p>However, bamboo plants are not universally admired. In fact many people are familiar with it because they have had a bad experience with it. Ask anyone who has a neighbor who grows one of the running types of bamboo. They are extremely difficult to control and often intrude on other yards.
</p>
<p>If you want to grow bamboo, choose from the many kinds of clumping bamboos that stay in place, expanding their root systems to form clumps that can easily be dug up and divided like ornamental grasses and perennials such as hostas.
</p>
<h4>Bamboo uses</h4>

<p>While you may never grow it, you will increasingly encounter bamboo in the store. Bamboo is one of the most easily recyclable wood products in the world. The tall stately types can grow up to 23 inches in one day. It is quickly renewable, grows in almost every climate and lasts for years. When it is discarded it decomposes in months. So it is, ironically, an environmentally desirable product.
</p>
<p>Among the hundreds of products made from bamboo these days (kitchen products, fabrics, tools) are birdfeeders and nesting boxes. Bamboo is easily colored with vegetable based dyes, so these products are colorful additions to the landscape. I have not tried any of them yet to evaluate particular styles, but why not check them out at the garden center?
</p>
<h4>Painters and plants</h4>

<p>Sooner or later the outside of the house, the garage, the fence or the pergola needs repainting or staining. Chances are these areas are also where plants are on display, so once again, your painting contractor is working in the zone -- where plants meet paint. Painting contractor, Steve Kapski, my expert who has lots of experience working in the zone has shared several observations with me.
</p>
<p>Homeowners can help speed the project and assure plant safety by planting any new foundation plants well away from walls. Choose compact shrubs that will not grow rampantly up against exterior walls, blocking access and possibly forcing time-consuming raising and lowering of extension ladders to get the job done.
</p>
<p>Prune any that are overgrown prior to the arrival of the painters. Also, never plant prickly plants such as firethorn (Pyracantha sp), or barberry in the zone. Train espaliered shrubs and vines onto trellises that can be lowered from the wall or fence and gently laid down on the ground temporarily to allow access by painters for power washing, painting or staining.
</p>
<p>These measures save your painters time and, thus, money, and they also insure that your plants stay healthy and strong.
</p>
<h4>Humor</h4>

<p>It is widely recognized that there is no strong tradition of plant and garden humor, even though lots of people care for plants, yards and gardens. When is the last time you heard a noted comic joke about plants or landscapes?
</p>
<p>Unfortunately it has been left to homeowners, gardeners and plant lovers of all kinds, to see the comedy in toiling in the yard, mowing the lawn, digging and planting, splitting firewood, shoveling snow and similar landscape activities. The truth is, we have not come up with great jokes. Sadly, we seem to have settled for puns and one-liners. (Warning: you may want to stop reading now.) If you can come up with something better, please send it to me to include in Yardening.
</p>

