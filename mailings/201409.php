<h4>State tree</h4>

<p>Quick now! What is Pennsylvania’s state tree? Yes, the Eastern Hemlock of which there are two native species (Tsuga canadensis and Tsuga caroliniana) in forests here along the East Coast.
</p>
<p>A fine-needled evergreen tree, it was named state tree in 1931. In the forests they provide unique habitat for breeding birds and certain fish such as brook trout because they moderate stream temperatures.
</p>
<p>Many variants of hemlock species also make lovely landscape plants. Some weep, others are statuesque, some are dwarf; some are comfortable in wet conditions, others are okay with drier soil.
</p>
<p>For good reason hemlocks have long been familiar trees in our parks and yards. Unfortunately, since 1951 this graceful tree has been ravaged by hemlock wooly adelgid (HWA). This aphid-like insect punctures the base of the needles on hemlock branches and sucks out nutrients, slowly killing the tree over five to 10 years.
</p>
<p>The telltale sign of infestation is small white “wooly” blobs at the base of the needles where the insect attacks. Many readers are all too familiar with this, I am sure. In our yards it is possible to control the wooly adelgid on one or two trees, but a major effort is also taking place in our forests. Research for more effective controls, biological and chemical is underway sponsored in part by the Nature Conservancy.
</p>
<h4>Redder</h4>

<p>Used to be, ornithologists regarded the rich red color on the feathers of a male bird such as a cardinal as a sign of vigor and health, signaling desirability as a mate. They assumed this because birds acquire their pigments, whether yellow, orange or red, from their traditional diets of berries full of carotinoids -- from native plants. Now researchers at Ohio State have discovered that introduced plants such as Amur honeysuckle also have berries rich in these pigments. So, the rich, red color of bird plumage is more an indicator of diet than of superior health.
</p>
<p>At McGill Bird Observatory in Montreal, researchers captured 15 Baltimore orioles that were way more red than classic orange. Guess what analysis of their feathers showed? The chemical in the berries of introduced honeysuckle species was in their feathers. So, do not be surprised to discover red birds at your feeder that you do not immediately recognize because they have been eating foreign cuisine.
</p>
<h4>Pervious pavement care</h4>

<p>In a previous column I discussed installation of various types of pavement for paths, walks and driveways that encourages rainwater to soak into the ground on the property rather than run off into storm sewers. Whether it is interlocking pavers, bricks, bluestone, gravel, pervious asphalt or concrete, these installations need a bit of maintenance over the years to make sure that sediment does not block proper drainage. Never apply sealants to pavers, bricks or stone in the installation. Always check the pavement after storms to make sure the water is not ponding or diverting into a nearby channel. Regularly sweep or rake leaves and lawn debris from the paving before it accumulates and blocks drainage.
</p>
<p>Do not add sand or cinders to melt snow on or near the pavement lest they block drainage. Remove snow with a rubber-tipped shovel or a plow edge that is one inch above the paved surface. Replace or add to the top layer of gravel between the joints of interlocking pavers periodically.
</p>
