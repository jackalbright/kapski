<h4> Poison ivy ID</h4>


<p>By early summer young poison ivy plants are quite visible and easy to remove. They are in your yard because their berries are beloved by more than 50 different species of over-wintering birds. The berries they ate last fall elsewhere fell onto your soil in their droppings, and have now germinated and sprouted the classic three-leafed pattern on thin stems.
</p>
<p>Over its lifetime, a poison ivy plant can vary enormously in color, shape and texture. So to correctly identify it, the key is the “leaves of three, let it be” rule. Leaves of immature plants typically have toothed edges to facilitate good water evaporation and growth.
</p>
<p>Often leaves are shiny, sometimes not. The stems may or may not be hairy. At puberty poison ivy leaves develop smooth edges, all the better to conserve moisture. With maturity, poison ivy leaves become larger, toothed and thicker textured.
</p>
<p>Plants have larger leaf stems, and the stem of the center leaf is a bit longer than those of the two side leaves.
</p>
<h4>Poison ivy RIP</h4>

<p>Check for young plants in the yard, especially near the air conditioner compressor. Birds are attracted to its fan, so may have left droppings there.
</p>
<p>Small, young plants are easy to deal with. Slip a plastic newspaper delivery bag over your hand and up your arm. Grab the young poison ivy plant and pull it, roots and all, from the soil. Then pull the plastic bag down over your arm and hand to trap the plant within the bag. Put it in the trash.
</p>
<p>As the plants grow, eliminating them from the yard becomes trickier. Even if you use a herbicide, you must still remove the dead plant. Remember, all parts of the plant contain a resin called urushiol and are still capable of causing a rash if they touch your skin. It has protected poison ivy plants from fungal attacks for more than 80 million years.
</p>
<p>Recent research has revealed that increased CO2 in the atmosphere is stimulating poison ivy plants to grow bigger and produce a more potent form of this resin.
</p>
<h4>In the zone</h4>

<p>Again, here are words of wisdom from painting contractor, Steve Kapski, who shares his experiences in the zone where painters meet landscapes.
</p>
<p>He cautions homeowners to choose carefully the organic mulch that will be spread near building foundations due for a fresh coat of exterior paint. One of his customers called him a few weeks after his team painted her house. She reported little black spots on the new paint job. Steve explained to her that they were caused by a fungus, called artillery or shotgun fungus, growing in the mulch. It propels its spores into the air when moisture conditions are suitable. They can land up to 5 feet or more on walls, fences, even nearby cars. They are extremely difficult to remove.
</p>
<p><strong>True or false</strong></p>

<p>Take a guess! Lots of native trees, shrubs or vines support wildlife.
</p>
<p>1. Blackberry, wild cherry and red mulberry serve the most fruit to the most bird species.</p>

<p>2. Pine, sweetgum, and birch provide the most seeds for the most bird species.
</p>
<p>3. A pile of twigs and young branches freshly fallen onto the ground from needled evergreen trees is probably the work of raccoons.
</p>
<p>Answers: True; True; False (squirrels)</p>
 
 
