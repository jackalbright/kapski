<h2>Air Sealing and Weatherization</h2>
<h3>Your holey home</h3>

<p>Leaving cracks and crevices for air to leak in and out of your home can make life feel drafty and uncomfortable. It can allow moisture-laden air to sneak inside, increasing the likelihood of condensation, mold, and rot. It can swell your energy demand, adding 20% to your bill for heating and cooling. To avoid this fate, prepare your house for winter and other sorts of inclement weather. In other words, weatherize!</p>

<p>Some people worry that a tighter house means poor indoor air quality. While it's true a healthy home needs fresh air, it's important to be able to control when and where that outdoor air enters and indoor air leaves.
</p>
<p>Many older homes are extremely leaky, and even newer homes often aren't airtight. You may be aware of drafty spots around doors and windows, but air is likely escaping in other places, too.</p>

