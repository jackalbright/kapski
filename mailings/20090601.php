<h3>Painting Tips from  Steve</h3> 
<p>Two similar houses on my street are being painted. One painter sprayed the stucco. The other is brushing and rolling the primer and paint onto the surface. </p>
<p>Which method is better? </p>
<p>We think brushing and rolling is always preferable. For one thing, the paint is better absorbed so it lasts longer. With spraying, some painters over-spray and you can end up with paint on your roof, cars, and even your neighbor's house. Many contractors who spray don't do proper prep either before applying paint.
</p><p> One of the most common mistakes in the prep phase is not tinting the primer. Some use white primer instead of tinting it to the color chosen for the topcoat. Your final color will be more solid and true, and when it starts failing over time, you will have another line of defense.
</p>
