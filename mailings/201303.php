<h3>How to get ready for Kapski Painters (and what to expect-
</h3> 
<p>This explanation of the Interior painting process includes what to expect from us and how to prepare your home for the painters. (A copy is given to the  client before starting the job.)
</p> 
<p>For best results please complete any renovations prior to the painting work. If you have scheduled us to do this work in advance of the painting we will assume the responsibility for the proper sequencing of the project.
</p> 
<p>If you are going to refinish the wood floors this should be done after the painting. The curing process of the floor coating usually takes a few weeksâ€™ time. The coating remains soft for some time and this limits use of stepladders etc. A quality floor contractor will be careful the walls and baseboard.
</p> 
<p>Please remove all of the smaller items from the workspaces. If we are doing multiple rooms we can offer suggestions on how best to accomplish this.
</p> 
