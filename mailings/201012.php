<h2>How to deal with a broken gutter in a storm</h2>



<p>When a torrential rain hits and a gutter gives way, sometimes the best tool for the job is a shovel, but really, prevent this by having Kapski clean and inspect your gutters and spouts.<br/>
Rainwater poured off the roof and into my face. I had gone up a ladder Monday morning intending to get the gutter back on, but it was proving to be a wrestling match.</p>
<p>I was one of many people caught up in small weather-related dramas as an odd storm tracked up the Eastern Seaboard bringing with it unseasonably warm temperatures, rain and howling wind with gusts up to 50 mph.</p>

<p>Three problems became obvious: The ladder was too short, the wind was too strong and the rain pouring off the roof made it almost impossible for me to see. The foul-weather gear that I was wearing did little good. Wind-driven rain ran down my face and neck, it blew into my cuffs and it blasted the hood off my head. In no time, the wool watch cap I was wearing was so wet that I could wring it out. When I let go of the gutter, it sliced back and forth in the wind like a pendulum gone wild.</p>
