<h3>Rising paint and raw materials costs</h3>
<p>As a consumer or end user to the painting industry, you may or may not aware of the rising costs of paint. Paint manufacturers are experiencing unprecedented raw material pressures, from increasing titanium dioxide and resin costs found in the formulation, to higher steel prices used in the containers.</p>
<p>These inflationary effects are making their way through the coatings chain and forcing end users to pay higher and higher prices for a gallon of paint. To make matters worse, this is occurring at a time when industry volumes are at decade level lows.</p>
<p>It amazes me that the industry has done little or nothing at all to spread the word as to exactly why this is!  In fact, two major manufacturers have seemed to shroud this information in a coat (pun intended) of secrecy.</p>
<p>This new reality may be a true reflection of the actual rising costs borne by the painting industry. Painting contractors need honest information from the manufacturers so that we can talk about pricing with our clients.</p>

