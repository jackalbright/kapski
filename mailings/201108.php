<h3>How to Choose a Paint Contractor for Your Home:</h3>
<p class="attribution">From the Paint Quality Institute</p>
<p>For residential work, more and more homeowners and tenants are choosing to use painting contractors rather than doing the work themselves. This is to save time and labor, and to achieve the best possible job.</p>

<p>When assessing a paint job, the potential do-it-yourselfer should consider such things as:< /p>

<ul>
<li>How much surface preparation is needed and will I have the time to do it?</li>
<li>Will I be able to do the more demanding aspects of the job such as climbing ladders, moving furniture, etc.?</li>
<li>Will I be able to live with a room or area left unfinished when I am called away, return to my regular job, etc.?</li>
</ul>
<p>When considering which contractor to utilize, be sure to do or determine each of the following:<br />
- Talk with several contractors and get quotes from each.<br />
- Ask about insurance and bonding.</p>

<p>Determine what will be done for each type of area (any wood trim, stucco, aluminum siding, walls of each room, etc.) for:</p>
<ul>
<li>Surface preparation</li>
<li>Priming; what primer (type and brand) will be used?</li>
<li>Finish coat: Type of paint and brand, where the brand fits into the manufacturer's line (premium, first, second, economy, contractor's grade, etc.)</li>
<li>How many coats?</li>
</ul>


<p>For exterior work, what will be done to protect plantings? For interior work, what will be done in regards to moving and/or protecting furniture?</p>
<ul>
<li>When will the work be started? </li>
<li>Finished? </li>
<li>How many painters will be working on the job?</li> 
<li>What will the remedy be if the job is not completed on time?</li>
<li>What is the warranty on the work? 
</li>What is the length of time, the type of failures covered, and the remedy if there is a paint failure?
</ul>
<p>Get names of previous customers from each contractor as references, and contact them to get their recommendation.
Note whether the contractor was courteous and business-like, punctual with appointments or in returning phone calls, quality-oriented, experienced, and generally interested in doing business with you.</p>

</div>

