<h3 class="storyBoxHeading">A Handy Tip</h3>

<p>For many years, I have worn leather gloves to work around the house and sometimes on the job and have found that the inexpensive, stiff gloves I used lasted just as long as the more expensive, soft and pliable ones.
Rather than spend more money for gloves, I have found a simple way to make the less expensive ones fit me more comfortably - while also helping heal my rough hands.</p>

<p>Here's my solution:</p>

<ol>
<li>Coat your hands with a liberal amount of the thickest hand cream you can find, then slip on the gloves and go about your work like you normally would when wearing gloves.</li>


<li>While you are working, the cream soaks into the leather, softens it and forms the gloves to your hands.</li>


<li>In a matter of minutes, the old, stiff gloves will become pliable and you won't be able to tell the inexpensive gloves from expensive ones.</li>
</ol>

<p>By the way, the lotion also helps keeps gloves that have gotten wet from becoming stiff when they dry out. The lotion also seems to make the gloves last a little longer.</p>
