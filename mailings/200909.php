<h3>Real Estate Appraisals</h3>

<p>New rules make it impossible to correct an appraisal before it is published and it ruins the deal. The pendulum has swung far too far.</p>

<p>I recently had heard of an appraisal for a custom home where the lot was valued at less than 50% of 12 very recent sales in the same subdivision. There were no short sales, no foreclosures, nothing bad - just an appraiser not doing his job and being overly conservative. (The sale price was in the mid-$60s for two acres and the appraisal was in the $30s.) He had already sold the lot and his customer had lot financing from a local bank based on a good local appraisal. He had an 80% lot loan of about $50,000.</p>

