<h3>We're Not Back</h3>


<p>We really never left. During the past few tough years we have transitioned pretty well, all things considered. Perhaps one of the reasons is that we have always done the small, but important jobs  for our customers. This has allowed us to be the first call when our customers had smaller projects that needed doing. Since we have  always done this, they call Kapski Painting for carpentry, stucco  and masonry repairs, door and window repairs, gutter work, adding  insulation, painting and paperhanging removal and installation. We still do the larger jobs, too.</p>
<p>Since the economic downturn we have become even more efficient, responsive and affordable.</p>
<p>And, because we have a roofer, plumber and electrician on our team,  one call or email does the trick. Even if it's just the bathroom or  a bedroom that needs painting , it can be done efficiently and effectively, again, with just one call or email.</p>
<p>So keep us in mind. Your neighbors have, and we thank them by providing the best service around.</p>
