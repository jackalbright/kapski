<h1>Ask Steve</h1>


<p><span class="QA">Q</span> Steve, how do you determine when a window is past its intended use in a historic building?</p>
<p><span class="QA">A</span> The windows on many old and historic houses are an important aspect of the architectural character of those buildings.
 
<p>Their design, craftsmanship, or other qualities may
make them worthy of preservation. This is self-evident for ornamental windows, but it can
be equally true for warehouses or factories where the windows may be the most dominant
visual element of an otherwise plain building.</p>
<p> Evaluating the significance of these windows and planning for their repair or replacement can be a complex process involving both objective
and subjective considerations.</p> 
