<h3 class="storyBoxHeading">Spring cleanup </h3>

<p>The calendar announces the official arrival of spring on the 20th. The brave crocus, snowdrops and early daffodils have appeared to encourage us that it is on the way. However, full-blown spring is often a long time arriving after this date. That does not mean though, that you can't start working outdoors in the yard to prepare for warmer weather.
</p>
<p>Clean up the landscape. Do a walkabout and pick up branches and other debris and examine trees and shrubs for winter damage.
</p>
<p>Welcome the emerging bulbs. Cut back the dried, brittle ornamental grass stems to the ground before the new shoots start. If the clump is getting too large, dig it up now and cut it into chunks to replant in place or elsewhere in the yard or to give away.
</p>
<p>Mow or cut back areas of groundcovers such as liriope or pachysandra that have browned and withered from the cold winter. As the new leaf shoots of hostas appear, clean up the old dried leaves on the ground around them. Divide them if necessary. Celebrate spring by planting some pansies!
</p>
</div> 

