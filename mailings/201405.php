<h4>Our clean-up procedure:</h4>

<p> We know that having people in your house is a disruption. We also know that leaving your house a mess is wrong so we have a clean-up procedure that we follow, for interior and exterior projects. We do this every day. If you need your room back and we aren't finished the room we'll remove our drop cloths, remove the new plastic film from the furniture and put back the furnishings. We'll clean the floors as well. We don't leave ladders and drop cloths in the way and we remove all of our debris daily.
</p> 
