<h3 class="storyBoxHeading">How to Prepare Your Home for Painting</h3>

<p>If you plan on painting the inside of your home, here's tips on how to prepare your home for the painters and what to expect from Kapski Painting.
</p> 
<p>For best results, please complete any renovations prior to the painting work. If you have scheduled us to do this work before the painting, we will assume the responsibility for the proper sequencing of the project.
</p> 
<p>If you are going to refinish the wood floors, this should be done AFTER the painting.
</p>
<p>The curing process of the floor coating usually takes a few weeks. The coating remains soft for some time and this limits our use of stepladders etc. A quality floor contractor will be careful of the walls and the baseboard.
</p> 
<p>Please remove all of the smaller items from the workspaces. If we are doing multiple rooms, we can offer suggestions on how best to accomplish this. The TV, DVD or entertainment center, and all associated wiring, should be removed from the work space.
</p>
<p>Smaller items such as photos, pictures hung on walls, carpeting, padding, lamps, lights, smaller furniture, nick-knacks, and window treatments should be removed too.
</p> 
