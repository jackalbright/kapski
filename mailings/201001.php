<h1 class="askSteve">Hey Steve:</h1>
<p>A question from  Jenn McDougall in Media, Pa, who asks:</p>
<p>Over the years as hose bibs and other items have been fastened to and
removed from the brick around the outside of my house, the number of holes
has grown. What's the best way to patch these holes so that they don't look
so obvious?</p>
<p >Well Jenn, you can fill these holes with a colored acrylic caulk that's close to the
color of your brick or stucco. But to get an exact match, try this trick:
Drill a shallow hole in one or your bricks in an unobtrusive spot using a
1/4 inch masonry bit. Collect the dust. Now just dab some colored or clear
caulk in each hole- I like to use a clear polymer sealant- then immediately
press the dust onto the caulk's sticky surface. When the caulk cures the
patches will be nearly invisible.</p>


