<h1>DIY Tips</h1>
<p>Keep the paint names for each room by writing the name of the color on a piece of masking tape  and putting on the back of the light switch plate!</p>


<p>Before you spackle nail holes, cut a cotton swab half and push it into the hole stick end first.  for larger holes fold the swab in half and use it all. then trim the excess. you won't need to go back for a second pass!</p>

