<h3 class="storyBoxHeading">Ask Steve</h3>
<p><span class="QA">Q</span>We want to remodel our newly purchased home. What are our options for switching from papered to painted walls?</p>
<p ><span class="QA">A</span>There are several options as follows:</p>

<p>For simplicity you may choose to leave it up and paint over it. However, before doing so, it needs quite a bit of prep work. Although the prep work is a bit extensive I feel that this is still far more efficient then installing new drywall or removing and prepping the wall under the removed wall paper for new paint.
</p>
<p>The first thing is we take a small 1.5 to 2 inch wide putty knife and lift up and remove any loose paper on the wall along the seams, ceiling and trim. We make sure that we keep peeling it until we reach a point where it wonâ€™t keep peeling. In some spots we end up removing a 2 x 2 foot square of material. After we are confident that we got all of the loose paper we go back and lightly sand those areas with 120 grit sand paper.
</p>
  
 

