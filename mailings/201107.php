<h3> Refinishing Fiberglass Doors: Mistakes to Avoid</h3>

<p>As your fiberglass doors get older, you may notice that the topcoat finish on them becomes worn, chipping and even flaking away, giving your door an aged and unattractive appearance. This is even truer of fiberglass doors that are on the exterior, as they have to face the elements as well as the natural aging process. There are a number of things that can be done to resurrect your door, and the most basic of these is the refinishing process. This can be done by even a home improvement amateur, although there are some things which should be avoided if you wish to get the best out of refinishing your doors.</p>

<p>1. Fiberglass Doors Not Cleaned Enough<br />
Doors which have been used for a while may have become dirty, and they will also have loose flakes of topcoat which need to be removed before they can be refinished. You may need to remove the layers of clear coat with a paint stripper. Once you have the coats removed, wet a damp sponge and add some dish soap. Wipe the door down thoroughly, and then cleanse with fresh water. Leave to dry, or dry quickly with a towel. Dirt and debris which is left on a door will become caught under the top coat, and will become pronounced, leaving unattractive bumps or lines where the topcoat has covered the door.</p>

<p>2. Finish Not Clear<br />
The most common kind of finish used on fiberglass doors is a topcoat known as Clear Coat. This is a translucent finish, which means that the staining of the door, and any wood grain effect this brings out, can be seen through the layers of finish. It is a mistake by some amateurs to use a pigmented finish, which hides the effect of the stain. Remember that you will be adding three or four layers of the topcoat, and so anything which is less than completely clear will have a build-up of color, leaving you with a door that is not the stained color you desired.</p>

<p>3. Finish Too Heavy<br />
Another problem which occurs when applying a topcoat is that the finish is put on too thickly, leaving a very heavy finish which will often be uneven. The finish may even travel over the door, and end up pooling at the bottom. Avoid this by only adding a little bit of topcoat at once. Don't put more than an inch of the paintbrush into the liquid at once, and apply in long, even strokes rather than short, rapid ones. Paint slowly, and take care that the finish is not dripping down the door.</p>

<p>4. Finish Not Dry<br />
More than one or two coats of finish need to be added to a door during the refinishing process. You should be sure to allow each coat to dry completely before you add another. Topcoats often need several days drying time, at least 48 hours, depending upon the humidity of the area. Test the topcoat in a hidden area before you proceed with the next coat, in order to ensure that it's fully dry before adding the next layer.</p>

