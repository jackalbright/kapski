<h3>Digital Color Wheel</h3>
<p>Choosing color is one of the most difficult decisions to make when decorating a room or an entire home. Color is integral to design and architecture, so choosing the right color and/or color combinations is important.
</p>
<p>Fortunately, there is color theory and the psychology of color to help you with these decisions.  Color theory is explained with this easy to use "Digital Color Wheel" from the Paint Quality Institute.</p>

<p>The Digital Color Wheel teaches you about different color schemes, i.e. which colors go with what. You can choose your primary or reference color, then move the mouse over the different color schemes, analogous, split complementary etc., to highlight all the colors that could complement your scheme.</p>

<p>There's also a color combinations tool which is fun to use if you're planning to mix your own colors, and a glossary to explain color terminology.
</p>
