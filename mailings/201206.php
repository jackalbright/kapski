<h3>Big design ideas for small kitchen spaces</h3>

by AraContent
<p>Bigger isn't always better in the real estate world, and what's more, it's not always possible. With more baby boomers downsizing from McMansions to smaller, more manageable houses, and a growing number of people opting to rent rather than buy, many families are looking for new ways to decorate, design and work with smaller spaces.
</p>
<img src="http://kapskipainting.com/images/kitchen_sm_red.jpg" width="194" height="259" border="0" align="left"  />
<p>Kitchens sell homes, but this most-used room in the house hasn't escaped the "less is more" trend, either. Interior designers, decorators and home product makers are responding to the growing demand for practical, beautiful options that make sense in smaller kitchens.
</p>
<p>If you're looking to make a big impact in a small kitchen, here are a few ideas to get you started:
</p>
<p>Work the walls - You may love your cozy kitchen, but that doesn't mean you want to make it look or feel any smaller than it already is. Lighter wall colors can help a little kitchen feel more open and airy. One winning decorating technique for small kitchens is to paint the majority of the wall space in a light neutral tone, like a pale cream or taupe and then add a pop of brighter or darker color to one accent wall or area. You can edge up the excitement of this technique by using a mural for kitchens on the accent wall, rather than just a different paint color. Have an oddly shaped niche in your kitchen? You can turn that problematic space into a design statement with custom, repositionable wallpaper that can be custom-sized and custom-colored to fit your specifications. This unique product can be adhered and re-adhered hundreds of times so you can take it with you to your next house if you choose.
</p>
<p>Liven things up with light - Often, a small kitchen will have just one window - or no window at all. Yet light is an important way to make a tight space feel more expansive and inviting. Task lighting is essential for work areas in small spaces. Pendant lights can create ambiance and provide practical illumination without sacrificing any wall space. And if you crave natural light for your windowless kitchen, consider installing a skylight. Tubular skylights can bring sunlight into your ground-floor kitchen even if there's a second floor above it.
</p>
<p>Small kitchens are here to stay, whether as part of the trend toward smaller, more efficient homes or the reality of rental unit life. With some decorating creativity and practical design tactics, you can ensure your small kitchen yields large benefits - both in terms of enjoyment and resale value. Contact Kapski Painting for advice on your small Kitchen space and to get a quote on what would work for your specific situation.
</p>


