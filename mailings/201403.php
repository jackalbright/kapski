<h3 class="storyBoxHeading">Let's Get Started!</h3>

<p>Can't wait to get started on that interior painting project? Need some inspiration? The Washington Post did a nice story on <a href="http://www.washingtonpost.com/lifestyle/home/expert-tips-for-choosing-the-right-paint-color/2014/02/26/a65e266a-9f27-11e3-b8d8-94577ff66b28_story.html">choosing the right interior color</a> and another about <a href="http://apps.washingtonpost.com/g/page/lifestyle/finding-the-right-white-paint/835/">finding the right white paint</a>. </p>
