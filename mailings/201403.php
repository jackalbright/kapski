<h3 class="storyBoxHeading">Case Study: Kitchen Makeover</h3>
<h4>Before:</h4>
<p>The home owner was getting negative feedback from potential buyers and this beautiful Chadds Ford house had been on the market too long. The realtor asked us to price up 3 coats of off-white enamel to hide the dark green cabinet and trim paint.
</p>

<img src="../images/newsletter/greenKitchen_before.jpg">

<h4>After:</h4>
<p><br>
We emailed photos daily to the client in California of the project's progress. Client and realtor are happy. There was an offer on the house one week after our completion.
The old green trim and cabinet color are sanded, washed, primed and painted with 2 coats of off white enamel. Beautiful transformation!
</p> 
<img src="../images/newsletter/whiteKitchen_after.jpg" >



<p>Give Kapski Painting a call at 610.565.8571. Let's give your home a fresh makeover for spring!</p>
