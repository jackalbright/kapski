<h1>March 2014</h1>
<h3 class="storyBoxHeading"></h3>
<p>
<p>No matter how long the winter, spring is sure to follow.  ~Proverb</p>

Why wait for spring to have your interior paint projects done? Low-VOC paints are great for painting inside during the winter months when you can't open windows, since they produce very little odor.
</p>

<p>Call us now at 610.565.8571 or email Steve at <a href="mailto:steve@kapskipainting.com" class="noborder">steve@kapskipainting.com</a> for an estimate on that indoor paint job.</p>
